using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace OOPs
{
    public class Calculation
    {
        public int Additiondata(int a, int b)
        {
            return a + b;
        }
        public int Additiondata(int a, int b, int c)
        {
            return a + b + c;
        }
        public double Additiondata(double a, double b)
        {
            return a + b;
        }
        public double Additiondata(int a, int b, double c)
        {
            return a + b + c;
        }
    }
